function calculatedistance(obj1, object2) {
  mydict = [];
  outputdict = {};
  Object.keys(obj1).forEach(function (key) {
    distance = calculateqdistance(
      obj1[key]["LATITUDE"],
      obj1[key]["LONGITUDE"],
      object2["latitude"],
      object2["longitude"]
    );
    mydict.push({ distance: distance, place: key });
  });

  mydict.sort(function (a, b) {
    return a.distance - b.distance;
  });
  for (i = 0; i < 10; i++) {
    outputdict[mydict[i].place] = obj1[mydict[i].place];
  }
  return outputdict;
}

function calculateqdistance(lat1, long1, lat2, long2) {
  return ((lat1 - lat2) ** 2 + (long1 - long2) ** 2) ** 1 / 2;
}

